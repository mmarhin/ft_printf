#include "libft.h"

int	ft_isalpha(int c)
{
	int	ok;

	ok = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		ok = 1;
	}
	return (ok);
}
